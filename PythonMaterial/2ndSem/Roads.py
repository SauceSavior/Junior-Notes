import turtle as t
wn=t.Screen()
t.speed(0)
def yellowLines(h,x,y,x2,y2):
    t.penup()
    t.setheading(h)
    t.goto(x,y)
    t.color("yellow")
    t.pensize(20)
    t.pendown()
    t.forward(300)
    t.penup()
    t.goto(x2,y2)
    t.color("gray")
    t.pensize(10)
    t.pendown()
    t.forward(300)
def outerLines(x,y,h):
    t.penup()
    t.color("white")
    t.goto(x,y)
    t.pensize(5)
    t.setheading(h)
    t.pendown()
    t.forward(400)
    t.left(90)
    t.forward(400)
def dashedLines(h,x,y,x2,y2):
    t.penup()
    t.setheading(h)
    t.goto(x,y)
    for x in range(5):
        t.pendown()
        t.forward(30)
        t.penup()
        t.forward(30)
    t.goto(x2,y2)
    for x in range(5):
        t.pendown()
        t.forward(30)
        t.penup()
        t.forward(30)
wn.bgcolor("grey")
yellowLines(270,0,-120,0,-100),yellowLines(180,-120,0,-100,0),yellowLines(90,0,120,0,100),yellowLines(0,120,0,100,0)
outerLines(-500,100,0),outerLines(100,500,270),outerLines(500,-100,180),outerLines(-100,-500,90)
dashedLines(0,120,50,120,-50),dashedLines(90,-50,120,50,120),dashedLines(180,-120,-50,-120,50),dashedLines(270,-50,-120,50,-120)
def lights():
    t.color("black")
    t.pendown()
    t.begin_fill()
    t.forward(50)
    t.right(90)
    t.forward(25)
    t.right(90)
    t.forward(50)
    t.right(90)
    t.forward(25)
    t.end_fill()
    t.penup()
    t.right(180)
    t.forward(19)
    t.left(90)
    t.forward(10)
    t.pendown()
    t.color("green")

    t.circle(6)
    t.penup()
    t.forward(15)
    t.pendown()
    t.color("yellow")
    t.circle(6)
    t.penup()
    t.forward(15)
    t.pendown()
    t.color("red")
    t.circle(6)
t.goto(95,95)
lights()
t.penup()
t.goto(-70,-45)
lights()
t.penup()
t.goto(-70,95)
lights()
t.penup()
t.goto(95,-45)
lights()
wn.mainloop()